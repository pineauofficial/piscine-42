#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	a;
	int	b;

	a = 1;
	b = 0;
	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	while (a < 46341)
	{
		b = a * a;
		if (b == nb)
			return (a);
		if (b > nb)
			return (0);
		a++;
	}
	return (0);
}
